package com.mycompany.atividadefor;

/**
 *
 * @author yasmn
 */
public class AtividadeFOR01 {

    public static void main(String[] args) {
        int soma = 0;  // Inicialização

        for (int i = 1; i <= 50; i++) {  // Loop que vai de 1 a 50
            soma += i;  // Adiciona o valor de i à variável soma
        }

        System.out.println("A soma dos números de 1 a 50 é: " + soma);    
    }
}
